<h2>FDND Logo's</h2>

<p>Yes, this is a very basic microsite. The purpose of this site is serving our
SVG logo to all other microsites we host which is a very important task. Please 
read all textual content to make sure you embed these logo's properly.</p>

<p>The first (and only as of now) option is a simple square SVG representative
of our logo which is similar to the logo used on all our websites. You can use 
the following code to embed our logo in your website.</p>

<pre>
  &lt;img src="https://fdnd.logo.nl/svg" /&gt;
</pre>
<picture>
  <img src="/svg" alt="Basic SVG version of the FDND logo" srcset="">
</picture>

<p>The name folder is optional and provides a way to control the 'name' of the
specific site which will be embedded in the logo:</p>

